newline = "Moattar is a god girl\nbut not a bad girl" # Newline
print(newline)

tab = "Moattar\tAnsari" # Adds a tab space.
print(tab)

backslash = "C:\\Users\\name" # Inserts a backslash (\) in the string.
print(backslash)

single_qoutes = "No It\'s a Sunny day" #Inserts a single quote (') inside a string enclosed in single quotes.
print(single_qoutes)

double_qoutes = "He said \"Hello\""
print(double_qoutes) # Prints the string with double quotes.

carriage_return = "Hello\rWorld" # Moves the cursor to the beginning of the line 
print(carriage_return)

backspace = "Hello\bWorld" #Moves the cursor one character backward
print(backspace)

form_feed = "Hello\fWorld" # Moves the cursor to the next page
print(form_feed)

vertical_tab = "Hello\vWorld" # Moves the cursor to the next vertical tab position.
print(vertical_tab)

unicode_space = "\u0041"
print(unicode_space) # Used to represent Unicode characters.

hexadecimal_escape_sequence = "\x41" # Represents a character by its hexadecimal value.
print(hexadecimal_escape_sequence)